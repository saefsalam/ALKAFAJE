--              🔧 إصلاح: السماح للعميل بإلغاء طلبه
--              لحل مشكلة عدم تحديث حالة الطلب عند الإلغاء
-- ═══════════════════════════════════════════════════════════════════════════════════════
-- 
-- المشكلة: العميل يستطيع إضافة سجل في order_status_history
--          لكن لا يستطيع تحديث status في جدول orders
-- 
-- الحل: إضافة سياسة تسمح للعميل بتحديث طلباته (فقط الإلغاء)
-- ═══════════════════════════════════════════════════════════════════════════════════════

-- حذف السياسة القديمة إن وجدت
DROP POLICY IF EXISTS "orders_update_own" ON public.orders;
DROP POLICY IF EXISTS "orders_cancel_own" ON public.orders;

-- ═══════════════════════════════════════════════════════════════════════════════════════
-- سياسة جديدة: العميل يستطيع تحديث طلباته
-- (سيُستخدم هذا لإلغاء الطلب فقط - التحقق الإضافي في الكود)
-- ═══════════════════════════════════════════════════════════════════════════════════════

CREATE POLICY "orders_update_own" ON public.orders
  FOR UPDATE 
  USING (customer_id = get_my_customer_id())
  WITH CHECK (customer_id = get_my_customer_id());

